package org.calminfotech.system.forms;

public class ExaminationSearchForm {
	
	private Integer examinationtypeId;
	
	private String mycriteria;
	
	private String mycriteriavalue;
	
	private Integer mysp;
	
	

	

	public Integer getExaminationtypeId() {
		return examinationtypeId;
	}

	public void setExaminationtypeId(Integer examinationtypeId) {
		this.examinationtypeId = examinationtypeId;
	}

	public Integer getMysp() {
		return mysp;
	}

	public void setMysp(Integer mysp) {
		this.mysp = mysp;
	}

	public String getMycriteria() {
		return mycriteria;
	}

	public void setMycriteria(String mycriteria) {
		this.mycriteria = mycriteria;
	}

	public String getMycriteriavalue() {
		return mycriteriavalue;
	}

	public void setMycriteriavalue(String mycriteriavalue) {
		this.mycriteriavalue = mycriteriavalue;
	}
	
	
}
	
	
	